# 비품지킴이 (AssetKeeper;비품을 안전하게 지키고 관리하는 시스템)DDL 
# 데이터베이스 준비 : db 서버의 여러개 테이블들을 저장할 수 있는 공간 
DROP DATABASE IF EXISTS AssetKeeper; -- 동일한 DB명이 존재하면 삭제
CREATE DATABASE AssetKeeper;         -- 지정한 DB명으로 데이터베이스 생성
USE AssetKeeper;                     -- 지정한 DB를 활성화/사용

CREATE TABLE assets (
    id INT PRIMARY KEY AUTO_INCREMENT, -- 비품의 고유 ID (관리번호)
    name VARCHAR(255) NOT NULL,        -- 비품명
    manager VARCHAR(100) NOT NULL,     -- 비품 관리자의 이름
    purchasePlace VARCHAR(255),        -- 구입처
    purchaseDate DATE,                 -- 구입일자
    purchasePrice DECIMAL(10,2),       -- 구입가격 (소수점 2자리)
    currentLocation VARCHAR(255),     -- 비품 현위치
    specialNotes TEXT                  -- 특이사항
);

# id: 비품의 고유 ID로 관리번호를 저장합니다. VARCHAR(20)로 설정하여 길이를 충분히 확보합니다.
# name: 비품명을 저장합니다. NOT NULL로 필수 입력 사항입니다.
# manager: 비품 관리자명을 저장합니다. NOT NULL로 필수 입력 사항입니다.
# purchasePlace: 구입처를 저장하는 열입니다.
# purchaseDate: 구입일자는 DATE 형식으로 저장됩니다.
# purchasePrice: 구입가격은 DECIMAL 형식으로, 소수점 2자리까지 저장됩니다.
# currentLocation: 비품이 현재 있는 위치를 저장합니다.
# specialNotes: 비품에 대한 특이사항을 저장하는 TEXT 형식입니다.

#데이터 삽입 테스트 예시

#데이터 조회 예시
SELECT * FROM assets;

#데이터 수정 예시
UPDATE assets
SET name = '수정된 비품명', manager = '새 관리자가', purchasePlace = '새 구입처', 
    purchaseDate = '2025-02-03', purchasePrice = 250000.00, currentLocation = '수정된 위치', 
    specialNotes = '새로운 특이사항'
WHERE id = 123; -- 숫자 비교


#데이터 삭제 예시
DELETE FROM assets
WHERE id = 123; -- 숫자 비교


